package hfdp2.chapter7_adapter_pattern.facade;

public class PopcornPopper {
    public void on() {
        System.out.println("Popcorn popper is on");
    }

    public void pop() {
        System.out.println("Popcorn popper pops");
    }

    public void off() {
        System.out.println("Popcorn popper is off");
    }
}
